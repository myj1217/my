package sec03.ex01;

public class Arithmetic {

	public static void main(String[] args) {
		int a = 10;
		int b = 5;
		
		// 덧셈
		int add = a + b; // 15
		
		// 뺄셈
		int subtract = a - b; // 5
		
		// 곱셈
		int multiply = a * b; // 50
		
		// 나눗셈
		int divide = a / b; // 2
		
		// 나머지
		int remainder = a % b; // 0
		
		// 증가 연산자
		int c = 5;
		c++; // 6
		
		// 감소 연산자
		int d = 7;
		d--; // 6
		
		System.out.println(c);
		System.out.println(d);
	}

}
